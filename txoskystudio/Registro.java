    public void registro() {
        int res = 0;
        PreparedStatement ps = null;
        Connection con = DArtistas.getConection();
        try {
            String sql = "INSERT INTO usuarios (usuario, password, nombre, email) VALUES (?,?,?,?)";
            ps = con.prepareStatement(sql);

            ps.setString(1, txf_user.getText());
            ps.setString(2, txp_pass.getText());
            ps.setString(3, txf_nombre.getText());
            ps.setString(4, txf_email.getText());
            res = ps.executeUpdate();

            if (res > 0) {
                JOptionPane.showMessageDialog(null, "Registrado con exito");
            } else {
                JOptionPane.showMessageDialog(null, "El registro no ha sido completado");
            }
            
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
        } finally {
            try {
                ps.close();
                con.close();
            } catch (SQLException ex) {
                System.out.println(ex.getMessage());
            }

        }
    }
    
